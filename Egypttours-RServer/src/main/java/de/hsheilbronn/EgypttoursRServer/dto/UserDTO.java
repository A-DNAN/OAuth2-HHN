package de.hsheilbronn.EgypttoursRServer.dto;

import de.hsheilbronn.EgypttoursRServer.model.user.UProfile;

import java.io.Serializable;

/**
 * @author ADNAN <ADNAN.E@TUTANOTA.DE>
 */

public class UserDTO implements Serializable {

    private String username;
    //Profile
    private String firstname;
    private String lastname;
    private String middle_name;
    private String pictureUrl;
    private UProfile.Gender gender;
    private String birthdate;
    private String email;
    //Address
    private String street;
    private String town;
    private String postalCode;
    private String country;
    //


    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getMiddle_name() {
        return middle_name;
    }

    public void setMiddle_name(String middle_name) {
        this.middle_name = middle_name;
    }

    public String getPictureUrl() {
        return pictureUrl;
    }

    public void setPictureUrl(String pictureUrl) {
        this.pictureUrl = pictureUrl;
    }

    public UProfile.Gender getGender() {
        return gender;
    }

    public void setGender(UProfile.Gender gender) {
        this.gender = gender;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getTown() {
        return town;
    }

    public void setTown(String town) {
        this.town = town;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
}